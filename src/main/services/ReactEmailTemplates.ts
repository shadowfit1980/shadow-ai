import { EventEmitter } from 'events';

export class ReactEmailTemplates extends EventEmitter {
    private static instance: ReactEmailTemplates;
    private constructor() { super(); }
    static getInstance(): ReactEmailTemplates {
        if (!ReactEmailTemplates.instance) {
            ReactEmailTemplates.instance = new ReactEmailTemplates();
        }
        return ReactEmailTemplates.instance;
    }

    generate(): string {
        return `// React Email Templates - MJML, React Email
// Generated by Shadow AI

import { Body, Container, Head, Html, Preview, Section, Text, Button } from '@react-email/components';
import { render } from '@react-email/render';

// Welcome Email
export function WelcomeEmail({ name, url }: { name: string; url: string }) {
    return (
        <Html>
            <Head />
            <Preview>Welcome!</Preview>
            <Body style={{ fontFamily: 'Arial', backgroundColor: '#f6f9fc' }}>
                <Container style={{ maxWidth: '600px', margin: '0 auto', padding: '20px' }}>
                    <Text style={{ fontSize: '24px' }}>Welcome, {name}!</Text>
                    <Button style={{ backgroundColor: '#3b82f6', color: '#fff', padding: '12px 24px' }} href={url}>
                        Get Started
                    </Button>
                </Container>
            </Body>
        </Html>
    );
}

// Render service
class EmailRenderService {
    welcome(name: string, url: string): string {
        return render(WelcomeEmail({ name, url }));
    }
}

export { WelcomeEmail, EmailRenderService };
`;
    }
}

export const reactEmailTemplates = ReactEmailTemplates.getInstance();
